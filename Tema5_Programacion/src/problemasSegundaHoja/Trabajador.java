package problemasSegundaHoja;

public class Trabajador {
	public int numId,titulacion;
	public boolean casado,turnoDia;
	// getters y setters
	public int getNumId() {
		return numId;
	}
	public void setNumId(int numId) {
		this.numId = numId;
	}
	public int getTitulacion() {
		return titulacion;
	}
	public void setTitulacion(int titulacion) {
		this.titulacion = titulacion;
	}
	public boolean isCasado() {
		return casado;
	}
	public void setCasado(boolean casado) {
		this.casado = casado;
	}
	public boolean isTurnoDia() {
		return turnoDia;
	}
	public void setTurnoDia(boolean turnoDia) {
		this.turnoDia = turnoDia;
	}
}
